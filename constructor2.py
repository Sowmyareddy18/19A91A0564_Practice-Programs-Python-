class Faculty:
    def __init__(self,n,s):
        self.name=n
        self.subject=s
    def print_details(self):
        print("Name :",self.name)
        print("Subject :",self.subject)
obj=Faculty("Devi priya","Python")
obj.print_details()
'''
Name : Devi priya
Subject : Python
'''

        
